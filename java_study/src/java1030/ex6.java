package java1030;

import java.io.BufferedReader;
import java.io.File;
import java.io.FileInputStream;
import java.io.FileReader;
import java.io.FileWriter;
import java.io.IOException;
import java.io.InputStreamReader;

public class ex6 {

	public static void main(String[] args) {
	// 텍스트 파일 복사하기
	File src = new File("C:/Users/admin/testFolder", "testFile.txt");
	File dest = new File("C:/Users/admin/testFolder", "testFile - 복사본.txt");
	
	if (!src.exists()) {
		System.out.println("복사할 파일 " + src.getName() + " 이 존재하지 않습니다.");
		return;
	}
	
	// 입력스트림 준비
	BufferedReader reader = null;
	
	// 출력스트림 준비
	FileWriter writer = null;
	
	
	try {
		reader = new BufferedReader(new InputStreamReader(new FileInputStream(src), "utf-8"));
//		reader = new BufferedReader(new FileReader(src)); // 한글 처리 못함
		writer = new FileWriter(dest);
		
		String line = "";
		while((line = reader.readLine()) != null) {
			writer.write(line + "\n"); // + "\n" 써줘야 엔터 쓰기까지 쓰기 가능
		}
		System.out.println("파일복사 완료");
	} catch (Exception e) {
		e.printStackTrace();
	} finally {
		try {
			reader.close();
			writer.close();
		} catch (IOException e) {
			// TODO Auto-generated catch block
			e.printStackTrace();
		}
		
	}

	}

}
